let carro = "ford";

switch(carro) {
    case "ferrari":
        console.log("O carro é uma Ferrari.");
        break;
    case "honda":
        console.log("O carro é um Honda.");
        break;
    case "mercedes":
        console.log("O carro é uma Mercedes.");
        break;
    case "ford":
        console.log("O carro é um Ford.");
        break;
    case "BMW":
        console.log("O carro é uma BMW.");
        break;
    case "mitsubichi":
        console.log("O carro é uma Mitsubichi.");
        break;
        default:
            console.log("Carro desconhecido.");
}